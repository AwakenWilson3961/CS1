public class Pemdas {
	public static void main (String [] args) {
		double firstCalculation = (9.5 * 4.5);
		
		double secondCalculation = (2.5 * 3);
				
		double thirdCalculation = (45.5 - 3.5);
		
		double result = firstCalculation - secondCalculation;
		
		double finalResult = result / thirdCalculation; 

		System.out.print(finalResult);
		
	}
	
}
